package com.canes.model;




public class Telefone {
    

  
    private Integer id;
    private String numero;

   

    public Telefone(){        
    }

    public Telefone( Integer id,String numero) {
        
        this.id = id;
        this.numero = numero;
        
    }

    public Integer getId() {
        return id;
    }

    
    public String getNumero() {
        return numero;
    }

    

    
    
   

   
   

    
}
